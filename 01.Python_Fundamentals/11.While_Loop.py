# While Loop

i = 0

while (i < 10) :
	
	i = i + 1
	print(i)
	
print()
	
# To increase any value by 1 we make the variable equal to variable + 1

# While Loop works till the condition is true

while (True) :
	
	i = i + 1
	print (i)
	break

print()

# While (True) is a command which runs for ever

# break command breaks the while loop

while (True) :
	
	if i < 5 :
		
		i = i + 1
		continue
		
	else :
		
		break

# continue command send the control back towards the start of while